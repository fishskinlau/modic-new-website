        <p class="text text-3">2015年來臨，第一句要說的話？ <br/>
身體健康！因為2014年曾經試過大病，如果病了，任面前放滿了甚麼美食或者發生著不同美事，也吃不了、享受不到，所以最想講的就是身體健康 。<br/><br/>

去年最難忘一件事？ <br/>
最難忘是可以放一個10天的假期，到了倫敦、巴黎，體驗了很多，還由羅浮宮行到凱旋門，認真難忘。<br/><br/>

羊年喜氣洋洋，自創以「羊」字作概念的祝福語？ <br/>
羊年來臨，祝做生意嘅朋友仔可以「羊」 名四海，到時自然就「羊」眉吐氣啦！<br/><br/>

入行經過？  <br/>
當初首先是集合了我和Otto，雖然我們長得帥，但也需要綠葉陪襯，因此就找來兩位師兄Eric和Osman，就組成了EO2。<br/><br/>

用一種顏色形容自己？ <br/>
灰色吧，不是因為我很「灰」，而是灰色是由黑色和白色結合， 神秘、剛強中，又帶點和暖。<br/><br/>

人生最需要的一種「養份」？<br/>
愛，是人生需要的養份，沒有愛的話，跟咸魚也沒分別了。</p>
