        <p class="text text-3">2015年來臨，第一句要說的話？ <br/>
2015年第一句要說的話是「阿彌陀佛」，希望大家身體健康、心想事成。<br/><br/>

去年最難忘一件事？ 
<br/>就是陪著周柏豪打到一個後空翻，因為本身我是一個很性急的人，也不喜歡教班、教人， 但這件事就正正可以訓練到我的耐性，明白有很多事情，不是一時三刻可以做到的，但當你奠定目標，立志實行的話…雖然是柏豪 在台上打這個後空翻，但亦令我有感而發，無論是藝人還是所有人，只要有了目標，信念與方向正確，如何去實踐、實行，到看到結果就時最有趣， 因此去年最難忘的事，是由年頭到柏豪的演唱會上，他能成功且完美地打了一個後空翻。<br/><br/>

羊年喜氣洋洋，自創以「羊」字作概念的祝福語？ <br/>
「羊千嬅」，三年抱兩，身體健康！<br/><br/>

入行經過？  <br/>
1999年11月11日，當時有三位排舞老師認為樂壇需要一隊跳舞組合，因此湊成了EO2；自己則由1995年9月參加無綫舞蹈藝員訓練班，在無綫經過了3年半時間後，組合了EO2，到現在成為了一位藝人，參與幕後、拍戲、拍劇及創作。
<br/><br/>
用一種顏色形容自己？ <br/>
年紀還小的時候，應該會很貪心地答「七彩」，因為想每個顏色、每件事亦能拿挰到，但到了這個年紀，我覺得一種代表幸福的顏色，這是每個人角度也不同的顏色，或者就是透明吧。<br/><br/>

人生最需要的一種「養份」？<br/>
說笑的話，會跟你說是錢，但實質不是，很概括是一種打不死的狀態，這種養分就是家人的支持。</p>
